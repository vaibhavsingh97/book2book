              <p class="flow-text">1. How was your experience of going from such a remote location to Delhi for buying/selling the

books?</p>

  <p>In my opinion selling/buying book at Nayi Sadak Market is really a very tough task. A week

  ago, I went to sell my 10 core subject books at Nayi Sadak Market. Firstly Nayi Sadak Market

  is around 2.5 hours away from our university and that is only possible when there is no

  traffic, so going there along with the a bag full of books was a hectic task as well as laborious

  task.</p>

   <p class="flow-text">2. How was your experience with the shopkeepers at Nayi Sadak, New Delhi?</p>

  <p>You will see and meet the real Delhi people when you start approaching to shops in order to

  sell your books because initially they show us like they are not interested to buy books by

  offering a least amount and they offered me 600 Rs only for 10 books Can you believe it?

  And there are so many shopkeepers which tell you the almost same price. So after visiting

  several book store we finally got a store which was giving us a decent amount compared to

  others in return of our books. </p>

   <p class="flow-text">3. What were expectation and for how much did you sell?</p>
  <p>I sold my 10 core subjects books which consists mainly of 5 th  and 4 th  semester books in

  exchange of Rs 1300 where as I was expecting around Rs 2000 at least because I paid

  somewhere around Rs 6000 to Rs 7000 for those 10 books. So expecting around 33% which

  is quite reasonable in my opinion but what I had receive is just Rs 1300 which is around 20%

  of the total cost of the books. I was initially offered Rs 1100 for my 10 books from the

  shopkeeper but after a negotiating for around more than 25 minutes he came to Rs 1300, as

  it wouldn’t be feasible for me to take back my books back to university so I finally accepted

  the deal at Rs 1300.</p>
  <p class="flow-text">4. Does buying is as difficult as selling of books at Nayi Sadak, New Delhi?</p>

  <p>After selling we moved to buying books for the 6 th  semester. So you will not find all the

  books at one store if you are looking for the 2 nd  hand books. You need to roam around shop

  to shop for finding the appropriate shop and getting the correct book. Now comes the

  pricing of the 2 nd  hand books. Again it’s up to you how much you can bargain for each book.

  For example I was getting a 2 nd  hand refrigeration and air conditioning book at a   price of Rs

  350 and the same book on amazon was of Rs 400. So you can get slightly cheaper books than

  the brand new ones but for that you need to spend your time and you need to have good

  negotiation skills else you will end up having nothing.</p>

   <p class="flow-text">5. So, how can you summarize your whole day, from university to Nayi Sadak and then coming

  back to the university?</p>

<p>  So, going there along with bag full of books and finding the appropriate shop and coming

  back was like almost a day or more than this. Apart from this the transport cost was

  somewhere around Rs 150 and you also need to eat something as you are away from your

  university for whole day. So going there and selling books was a very laborious task and

  apart from this not getting the appropriate price was like coming back with empty hands .</p>
