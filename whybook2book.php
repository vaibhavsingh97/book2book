                 How was your experience of selling books to the shopkeepers and at Nayi Sadak, New Delhi?

                I went to Nayi Sadak on a Sunday morning with few of my friends. It was very congested. The

                shops were on the either side of the roads, roads being 6-7 feet wide complimented by

                some road side shopkeepers keeping their stalls by the side of the road and vehicles parked

                in front of store which made it more congested, so, carrying books in that market was

                literally a headache who is not familiar with that market. Selling books there was not an easy

                task neither purchasing the books which we want, the main issues being cost. First of all, the

                shopkeepers were not willing to pay the genuine price of the books (they rated around 10-

                15% of the original cost) for all the books. So, we had to roam here and there with that

                heavy luggage to find appropriate rates, we found a shop after roaming around and hour

                (22% which is still below the rates seeing the conditions and edition of the books) but we

                were feeling tired of carrying that luggage so we laid down our weapon.</p>

